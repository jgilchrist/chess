pub fn piece_square_tables(game: &Game) -> Eval {
    piece_square_tables_white(game) + piece_square_tables_black(game)
}

fn piece_contribution(pieces: Squares, piece_table: &PieceValueTable) -> i32 {
    pieces.iter()
        .map(|p| piece_table[p.array_idx()])
        .sum()
}

fn piece_square_tables_white(game: &Game) -> Eval {
    let pawn_score = piece_contribution(game.board.white_pieces.pawns, &PAWN_TABLE_WHITE);
    let knight_score = piece_contribution(game.board.white_pieces.knights, &KNIGHT_TABLE_WHITE);
    let bishops_score = piece_contribution(game.board.white_pieces.bishops, &BISHOP_TABLE_WHITE);
    let rook_score = piece_contribution(game.board.white_pieces.rooks, &ROOK_TABLE_WHITE);
    let queen_score = piece_contribution(game.board.white_pieces.queens, &QUEEN_TABLE_WHITE);
    let king_score = piece_contribution(game.board.white_pieces.king, &KING_TABLE_WHITE);

    Eval(
        pawn_score
            + knight_score
            + bishops_score
            + rook_score
            + queen_score
            + king_score
    )
}

fn piece_square_tables_black(game: &Game) -> Eval {
    let pawn_score = piece_contribution(game.board.black_pieces.pawns, &PAWN_TABLE_BLACK);
    let knight_score = piece_contribution(game.board.black_pieces.knights, &KNIGHT_TABLE_BLACK);
    let bishops_score = piece_contribution(game.board.black_pieces.bishops, &BISHOP_TABLE_BLACK);
    let rook_score = piece_contribution(game.board.black_pieces.rooks, &ROOK_TABLE_BLACK);
    let queen_score = piece_contribution(game.board.black_pieces.queens, &QUEEN_TABLE_BLACK);
    let king_score = piece_contribution(game.board.black_pieces.king, &KING_TABLE_BLACK);

    -Eval(
        pawn_score
            + knight_score
            + bishops_score
            + rook_score
            + queen_score
            + king_score
    )
}
